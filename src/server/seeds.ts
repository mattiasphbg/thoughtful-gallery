import { PrismaClient } from "@prisma/client";

const prisma = new PrismaClient();

async function main() {
  await prisma.user.create({
    data: {
      name: "Eloise",
      jobTitle: "Programmer",
      email: "Hello@some.com",
      posts: {
        create: {
          title: "How to create a MySQL database",
          content: "Some content",
        },
      },
    },
  });
}
main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });
